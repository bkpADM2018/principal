SELECT CPROR1 as Producto, CSUCR1 as Sucursal, COPER1 as Operacion, NCTOR1 as Numero, ACOSR1 as Cosecha, KGNEFA as KilosDescarga, CONCAT(PPIAFA, NPIAFA) AS Comprobante
FROM MERFL.MER311F1
inner JOIN MERFL.MER5F1F1 ON CPROR1=CPROFA and CSUCR1=CSUCfa and COPER1 =COPEfa AND NCTOR1=NCTOfa AND ACOSR1=ACOSfa
WHERE CPROR1 =15 and COPER1 in (00, 01, 09, 10) and ACOSR1 = 07 and FCCTR1<20070112
order by CPROR1, CSUCR1, COPER1, NCTOR1, ACOSR1