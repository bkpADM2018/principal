SELECT Distinct CPROR1 as Producto, CSUCR1 as Sucursal, COPER1 as Operacion, NCTOR1 as Numero, ACOSR1 as Cosecha, DSUUNB as KilosPagados,  DSQGNB as F1116B
FROM MERFL.MER311F1
INNER JOIN MERFL.MER331F1 ON CPROR1=CPROCC and CSUCR1=CSUCCC and COPER1 =COPECC AND NCTOR1=NCTOCC AND ACOSR1=ACOSCC
INNER JOIN PROVFL.ACDSREP ON DSQFNB = NCOMCC
WHERE CPROR1 in (15, 19) and COPER1 in (00, 01, 09, 10) and ACOSR1 = 07 AND CCOMCC=60 AND FCCTR1<20070112 AND DSDUST='BDB'  and DSQPNB=0
order by CPROR1, CSUCR1, COPER1, NCTOR1, ACOSR1